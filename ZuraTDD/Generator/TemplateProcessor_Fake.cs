using ZuraTDD.Generator.DataModel;
using System.Linq;

namespace ZuraTDD.Generator;

internal partial class TemplateProcessor
{
	public static string MockedTypeClassCode(MockedTypeSpecification mockedType)
	{
		var methods = mockedType.Methods
			.Select(m => Functions.GenerateMethodMockingCode(mockedType, m));

		var methodsCode = string.Join("\n\n", methods);

		var genericTypeParams = mockedType.TypeInfo.GetMethodGenericTypeParametersString();
		var genericParamConstraints = mockedType.TypeInfo.GetMethodGenericTypeParameterConstraints("\n\t")
			.PrependNotEmpty("\n\t");

		return
			$$"""
			// <auto-generated />
			#nullable enable
			using ZuraTDD;
			using ZuraTDD.BuildingBlocks;
						
			namespace {{mockedType.OutputNamespace}};

			/// <summary>
			/// A mock implementation of <see cref="{{mockedType.TypeInfo.FullyQualifiedGenericTypeName}}" />.
			/// </summary>
			internal class {{mockedType.MockedFakeTypeName}}{{genericTypeParams}}
				: MockedObject
				, {{mockedType.TypeInfo.FullyQualifiedGenericTypeName}}{{genericParamConstraints}}
			{
				public {{mockedType.MockedFakeTypeName}}(
					params IEnumerable<BehaviorSetup> behaviors)
					: base(behaviors)
				{
				}

				public {{mockedType.ExpectTypeName}} GetExpectObject()
				{
					return new(this);
				}

			{{methodsCode}}
			}
			""";
	}
}

static file class Functions
{
	public static string GenerateMethodMockingCode(
		MockedTypeSpecification mockedType,
		MethodSpecification mockedMethod)
	{
		var @return = mockedMethod.ReturnType == "void" ? "" : "return ";
		var genericParameters = mockedMethod.GetMethodGenericTypeParametersString();
		var genericParameterTypes = mockedMethod.GetMethodGenericTypeParametersTypeofs();
		var paramValues = mockedMethod.GetParamValuesString();
		var defaultResult = mockedMethod.GetDefaultResult().PrependNotEmpty(",\n\t\t\t");
		var genericTypeParamConstraints = mockedMethod.GetMethodGenericTypeParameterConstraints()
			.PrependNotEmpty("\n\t\t");

		return
			$$"""
				/// <summary>
				/// Simulates behavior for <see cref="{{mockedType.TypeInfo.FullyQualifiedGenericTypeName}}.{{mockedMethod.MethodName}}" />.
				/// </summary>
				public {{mockedMethod.ReturnType}} {{mockedMethod.MethodName}}{{genericParameters}}({{ParameterDeclarations(mockedMethod)}}){{genericTypeParamConstraints}}
				{
					this.CallTracker.ReceiveCall(
						{{mockedType.MockedTypeMethodsTypeName}}.{{mockedMethod.MethodCodeName}},
						[{{paramValues}}],
						[{{genericParameterTypes}}]);

					#pragma warning disable CS8604
					{{@return}}base.BehaviorSetupRunner.{{InvokeMethod(mockedMethod)}}(
						{{mockedType.MockedTypeMethodsTypeName}}.{{mockedMethod.MethodCodeName}}{{paramValues.PrependNotEmpty(",\n\t\t\t")}},
						[{{genericParameterTypes}}]{{defaultResult}});
					#pragma warning restore CS8604
				}
			""";
	}

	private static string ParameterDeclarations(MethodSpecification method)
	{
		if (method.Parameters.Count == 0)
			return "";

		var declarations = method.Parameters
			.Select(p => $"{p.Type} {p.Name}");

		return "\n\t\t" + string.Join(",\n\t\t", declarations);
	}

	private static string InvokeMethod(MethodSpecification method)
	{
		return method.ReturnType == "void"
			? $"InvokeActionBehavior{method.GetGenericParamsString()}"
			: $"InvokeFuncBehavior{method.GetGenericParamsString()}";
	}
}
